from __future__ import annotations

import json
import textwrap
from pathlib import Path

import pytest

from specify_cli.doctrine.pack_assembler import (
    AssemblyResult,
    assemble_pack,
    render_assembly_result,
)


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------


pytestmark = [pytest.mark.unit, pytest.mark.fast]

def _make_pack(
    root: Path,
    name: str,
    *,
    directives: list[str] | None = None,
) -> Path:
    """Create a minimal valid pack with the given directive IDs."""
    pack = root / name
    pack.mkdir(parents=True, exist_ok=True)
    if directives:
        dir_dir = pack / "directives"
        dir_dir.mkdir(parents=True, exist_ok=True)
        for artifact_id in directives:
            (dir_dir / f"{artifact_id.lower()}.directive.yaml").write_text(
                textwrap.dedent(
                    f"""\
                    schema_version: "1.0"
                    id: {artifact_id}
                    title: {artifact_id} title
                    intent: Generated by tests.
                    enforcement: advisory
                    """
                ),
                encoding="utf-8",
            )
    return pack


def _add_drg_fragment(
    pack: Path, name: str, *, source: str, target: str
) -> None:
    drg = pack / "drg"
    drg.mkdir(parents=True, exist_ok=True)
    (drg / name).write_text(
        textwrap.dedent(
            f"""\
            schema_version: "1.0"
            generated_at: STATIC
            generated_by: test
            nodes: []
            edges:
              - source: {source}
                target: {target}
                relation: requires
            """
        ),
        encoding="utf-8",
    )


