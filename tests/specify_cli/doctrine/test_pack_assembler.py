class TestAssemblePack:
    def test_single_pack(self, tmp_path: Path) -> None:
        pack = _make_pack(tmp_path, "alpha", directives=["A-001", "A-002", "A-003"])
        output = tmp_path / "out"

        result = assemble_pack([pack], output)

        assert isinstance(result, AssemblyResult)
        assert result.ok is True, result.errors
        assert result.artifacts_written == 3
        assert (output / "directives").is_dir()
        assert len(list((output / "directives").glob("*.directive.yaml"))) == 3
        assert (output / "pack-manifest.yaml").is_file()

    def test_two_packs_no_conflict(self, tmp_path: Path) -> None:
        a = _make_pack(tmp_path, "alpha", directives=["A-001"])
        b = _make_pack(tmp_path, "bravo", directives=["B-001"])
        output = tmp_path / "out"

        result = assemble_pack([a, b], output)

        assert result.ok is True, result.errors
        assert result.artifacts_written == 2
        files = list((output / "directives").glob("*.directive.yaml"))
        assert {f.name for f in files} == {
            "a-001.directive.yaml",
            "b-001.directive.yaml",
        }

    def test_id_conflict_blocks(self, tmp_path: Path) -> None:
        a = _make_pack(tmp_path, "alpha", directives=["DUP-001"])
        b = _make_pack(tmp_path, "bravo", directives=["DUP-001"])
        output = tmp_path / "out"

        result = assemble_pack([a, b], output)

        assert result.ok is False
        assert result.artifacts_written == 0
        assert result.conflicts, "expected at least one conflict"
        names = {pack for c in result.conflicts for pack in c.conflicting_packs}
        assert {"alpha", "bravo"}.issubset(names)
        assert not output.exists(), "output dir must not be created on conflict"

    def test_force_resolves_conflict(self, tmp_path: Path) -> None:
        a = _make_pack(tmp_path, "alpha", directives=["DUP-002"])
        b = _make_pack(tmp_path, "bravo", directives=["DUP-002"])
        # Mutate bravo's file so we can verify last-pack-wins.
        bravo_file = b / "directives" / "dup-002.directive.yaml"
        bravo_file.write_text(
            textwrap.dedent(
                """\
                schema_version: "1.0"
                id: DUP-002
                title: BRAVO TITLE
                intent: From bravo.
                enforcement: advisory
                """
            ),
            encoding="utf-8",
        )
        output = tmp_path / "out"

        result = assemble_pack([a, b], output, force=True)

        assert result.ok is True, result.errors
        assert result.artifacts_written == 1
        produced = (output / "directives" / "dup-002.directive.yaml").read_text(
            encoding="utf-8"
        )
        assert "BRAVO TITLE" in produced

    def test_drg_conflict(self, tmp_path: Path) -> None:
        a = _make_pack(tmp_path, "alpha", directives=["X-001", "X-002"])
        b = _make_pack(tmp_path, "bravo", directives=["Y-001", "Y-002"])
        # Same edge defined in both packs.
        _add_drg_fragment(
            a, "010.graph.yaml", source="directive:X-001", target="directive:X-002"
        )
        _add_drg_fragment(
            b, "010.graph.yaml", source="directive:X-001", target="directive:X-002"
        )
        output = tmp_path / "out"

        result = assemble_pack([a, b], output)

        assert result.ok is False
        drg_conflicts = [c for c in result.conflicts if c.artifact_type == "drg"]
        assert drg_conflicts, result.conflicts

    def test_conflicts_out_written(self, tmp_path: Path) -> None:
        a = _make_pack(tmp_path, "alpha", directives=["DUP-003"])
        b = _make_pack(tmp_path, "bravo", directives=["DUP-003"])
        output = tmp_path / "out"
        conflicts_out = tmp_path / "conflicts.json"

        result = assemble_pack(
            [a, b], output, conflicts_out=conflicts_out
        )

        assert result.ok is False
        assert conflicts_out.is_file()
        payload = json.loads(conflicts_out.read_text(encoding="utf-8"))
        assert isinstance(payload, list)
        assert payload, "conflicts JSON must be non-empty on conflict"
        assert payload[0]["artifact_id"] == "DUP-003"

    def test_assembled_pack_is_validated(self, tmp_path: Path) -> None:
        # When the merged pack would be invalid, the assembler must clean up
        # and return ok=False.  We simulate this by feeding in a pack whose
        # directive is *individually* valid but produces a dangling DRG edge
        # in the merged output.
        a = _make_pack(tmp_path, "alpha", directives=["Q-001"])
        _add_drg_fragment(
            a,
            "010.graph.yaml",
            source="directive:Q-001",
            target="directive:NEVER-DEFINED",
        )
        output = tmp_path / "out"

        result = assemble_pack([a], output)

        assert result.ok is False
        assert not output.exists()
        assert any("validation" in e for e in result.errors)

    def test_no_input_packs_is_error(self, tmp_path: Path) -> None:
        result = assemble_pack([], tmp_path / "out")
        assert result.ok is False
        assert "no input packs" in " ".join(result.errors).lower()

    def test_missing_input_pack_is_error(self, tmp_path: Path) -> None:
        ghost = tmp_path / "ghost"
        result = assemble_pack([ghost], tmp_path / "out")
        assert result.ok is False
        assert any("not found" in e for e in result.errors)

    def test_force_refuses_to_delete_non_pack_output_dir(
        self, tmp_path: Path
    ) -> None:
        pack = _make_pack(tmp_path, "alpha", directives=["SAFE-001"])
        output = tmp_path / "important-data"
        output.mkdir()
        marker = output / "do-not-delete.txt"
        marker.write_text("keep\n", encoding="utf-8")

        result = assemble_pack([pack], output, force=True)

        assert result.ok is False
        assert "refusing to delete non-pack" in " ".join(result.errors)
        assert marker.read_text(encoding="utf-8") == "keep\n"

    def test_force_refuses_spoofed_minimal_pack_manifest(
        self, tmp_path: Path
    ) -> None:
        pack = _make_pack(tmp_path, "alpha", directives=["SAFE-002"])
        output = tmp_path / "important-data"
        output.mkdir()
        (output / "pack-manifest.yaml").write_text(
            "source_type: assemble\n",
            encoding="utf-8",
        )
        marker = output / "do-not-delete.txt"
        marker.write_text("keep\n", encoding="utf-8")

        result = assemble_pack([pack], output, force=True)

        assert result.ok is False
        assert "refusing to delete non-pack" in " ".join(result.errors)
        assert marker.read_text(encoding="utf-8") == "keep\n"

    def test_force_allows_replacing_previous_pack_output(
        self, tmp_path: Path
    ) -> None:
        first = _make_pack(tmp_path, "alpha", directives=["OLD-001"])
        second = _make_pack(tmp_path, "bravo", directives=["NEW-001"])
        output = tmp_path / "out"

        first_result = assemble_pack([first], output)
        assert first_result.ok is True, first_result.errors

        second_result = assemble_pack([second], output, force=True)

        assert second_result.ok is True, second_result.errors
        assert not (output / "directives" / "old-001.directive.yaml").exists()
        assert (output / "directives" / "new-001.directive.yaml").exists()


